    private void HienThiTabale() {
        String[] hearder = {"Mã CTSP", "Mã PN", "Số lượng", "Giá nhập", "Thành tiền"};
        DefaultTableModel model = new DefaultTableModel(hearder, 0);
        for (CT_PhieuNhap m : dsctpn) {
            Object[] o = {m.getMaCTSP(), m.getMaPN(), m.getSoLuong(), m.getGiaNhap(), m.getThanhTien()};
            model.addRow(o);
        }
        tblDanhSachPhieuNhap.setModel(model);

        int rowCount = tblDanhSachPhieuNhap.getRowCount();
        if (rowCount > 0) {
            if (vitri < 0 || vitri >= rowCount) {
                vitri = 0;
            }
            tblDanhSachPhieuNhap.setRowSelectionInterval(vitri, vitri);
        }
}

