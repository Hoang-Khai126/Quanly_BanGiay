    public void loadcmbTenNV() throws SQLException {
        String sql = "select * from NhanVien";
        ResultSet rs = SqlHelper.executeQuery(sql);
        DefaultComboBoxModel cmbModel = (DefaultComboBoxModel) cmbTenNV.getModel();
        cmbModel.removeAllElements();
        try {
            while (rs.next()) {
                String MaNV = rs.getString("MaNV");
                String TenNV = rs.getString("TenNV");
                String DiaChi = rs.getString("DiaChi");
                String GioiTinh = rs.getString("GioiTinh");
                String Sdt = rs.getString("Sdt");
                String Email = rs.getString("Email");
                cmb_TenNV mycmb = new cmb_TenNV(MaNV, TenNV, DiaChi, GioiTinh, Sdt, Email);
                cmbModel.addElement(mycmb);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(new JFrame(), "Lỗi lấy tên nhân viên !!!");
        }
    }

